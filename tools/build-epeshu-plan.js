#!/usr/bin/env node
// build-epeshu-plan.js — fit prototypes/data/epeshu-trace.json (canon chart pixel
// space) into the sim's settlement-local frame and emit the EPESHU_PLAN block for
// index.html (regenerate, don't hand-edit).
//   node tools/build-epeshu-plan.js            → prints the JS block
// Frame: meters, origin at the Epēshīn Forum plaza centroid (the sim's market
// square), +x east, +z south — the chart's own orientation (its west coast faces
// the sim's White Sea). Scale fits the built extent inside the capital's growth
// reach (~260 m radius).
'use strict';
const fs = require('fs');
const t = JSON.parse(fs.readFileSync('prototypes/data/epeshu-trace.json', 'utf8'));

const forum = t.plazas.find(p => /Forum/i.test(p.name)) || t.plazas[0];
const cen = poly => poly.reduce((a, p) => [a[0]+p[0]/poly.length, a[1]+p[1]/poly.length], [0,0]);
const [ox, oy] = cen(forum.pts);

// scale: farthest extent vertex from the forum → 260 m
let maxR = 0;
t.extent.forEach(p => { maxR = Math.max(maxR, Math.hypot(p[0]-ox, p[1]-oy)); });
const S = 260 / maxR;

const XY = p => [ +( (p[0]-ox)*S ).toFixed(1), +( (p[1]-oy)*S ).toFixed(1) ];
// Douglas-Peucker to keep the block small
function dp(pts, eps){
  if(pts.length < 3) return pts;
  const [a, b] = [pts[0], pts[pts.length-1]];
  let mi = 0, md = 0;
  for(let i = 1; i < pts.length-1; i++){
    const t2 = ((pts[i][0]-a[0])*(b[0]-a[0])+(pts[i][1]-a[1])*(b[1]-a[1])) /
               (Math.hypot(b[0]-a[0], b[1]-a[1])**2 || 1);
    const q = [a[0]+(b[0]-a[0])*Math.max(0,Math.min(1,t2)), a[1]+(b[1]-a[1])*Math.max(0,Math.min(1,t2))];
    const d = Math.hypot(pts[i][0]-q[0], pts[i][1]-q[1]);
    if(d > md){ md = d; mi = i; }
  }
  if(md <= eps) return [a, b];
  return dp(pts.slice(0, mi+1), eps).slice(0, -1).concat(dp(pts.slice(mi), eps));
}
const simplify = (poly, eps) => dp(poly.map(XY), eps);

const plan = {
  primaries: t.primaries.map(r => ({ n: r.name, p: simplify(r.pts, 6), w: Math.max(3.6, Math.min(6, r.w*S)) })),
  extent: simplify(t.extent, 8),
  plazas: t.plazas.map(p => ({ n: p.name, p: simplify(p.pts, 4) })),
  masks: t.masks.map(m => ({ n: m.name, p: simplify(m.pts, 8) }))
};

const nPts = o => JSON.stringify(o).length;
console.error(`plan: ${plan.primaries.length} primaries, extent ${plan.extent.length} pts, ` +
  `${plan.plazas.length} plazas, ${plan.masks.length} masks, ~${(nPts(plan)/1024).toFixed(1)}KB, scale ${S.toFixed(4)} m/px`);

console.log('// Epēshu street plan traced from the canon city chart — generated by');
console.log('// tools/build-epeshu-plan.js from prototypes/data/epeshu-trace.json;');
console.log('// metres, origin = Epēshīn Forum (market square), +x east +z south. Regenerate, don\'t hand-edit.');
console.log('const EPESHU_PLAN = ' + JSON.stringify(plan) + ';');
